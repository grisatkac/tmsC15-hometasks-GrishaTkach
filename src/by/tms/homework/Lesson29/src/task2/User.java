package task2;

import task2.utils.IdentityUtil;
import task2.utils.UserUtil;

import java.io.Serial;
import java.io.Serializable;
import java.util.Objects;

public class User implements Serializable {

    @Serial
    private static final long serialVersionUID = 6878079612609578438L;
    private long id;
    private String name;
    private int age;

    public User() {
        this.id = IdentityUtil.generateUserId();
        this.name = UserUtil.generateRandomUserName();
        this.age = UserUtil.generateRandomUserAge();
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (!(o instanceof User)) return false;
        User user = (User) o;
        return age == user.age && Objects.equals(name, user.name);
    }

    @Override
    public int hashCode() {
        return Objects.hash(name, age);
    }

    @Override
    public String toString() {
        return "User{" +
                "name='" + name + '\'' +
                ", age=" + age +
                '}' + '\n';
    }
}
